  // Resolves a translation key and replaces any {placeholders}.
  String translate(String key, {Map<String, String>? params}) {
    final languageCode = locale.languageCode;
    final localizedMap = _localizedValues[languageCode] ?? {};
    final defaultMap = _localizedValues['en'] ?? {};
    String value = localizedMap[key] ?? defaultMap[key] ?? key;
    if (params != null && params.isNotEmpty) {
      params.forEach((placeholder, replacement) {
        value = value.replaceAll('{$placeholder}', replacement);
      });
    }
    return value;
  }

  // Delegate wired into Flutter localization system.
  static const LocalizationsDelegate<AppLocalizations> delegate =
      _AppLocalizationsDelegate();
}

// Delegate implementation for loading AppLocalizations.
class _AppLocalizationsDelegate
    extends LocalizationsDelegate<AppLocalizations> {
  const _AppLocalizationsDelegate();

  @override
  // True when the locale is in supportedLocales.
  bool isSupported(Locale locale) => AppLocalizations.supportedLocales
      .map((l) => l.languageCode)
      .contains(locale.languageCode);

  @override
  // Creates the localization instance for the locale.
  Future<AppLocalizations> load(Locale locale) async {
    return AppLocalizations(locale);
  }

  @override
  // No reload needed since values are static.
  bool shouldReload(covariant LocalizationsDelegate<AppLocalizations> old) =>
      false;
}

// Convenience extensions for translating within widgets.
extension LocalizationBuildContext on BuildContext {
  AppLocalizations get l10n => AppLocalizations.of(this);

  // Shorthand for translating a key with optional parameters.
  String tr(String key, {Map<String, String>? params}) =>
      l10n.translate(key, params: params);
}

// Convenience extension for translating a String key.
extension LocalizationString on String {
  String tr(BuildContext context, {Map<String, String>? params}) =>
      AppLocalizations.of(context).translate(this, params: params);
}
